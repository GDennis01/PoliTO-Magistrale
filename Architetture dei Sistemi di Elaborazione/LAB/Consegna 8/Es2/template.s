SVC_Handler     PROC
                EXPORT  SVC_Handler               [WEAK]
				
num				RN 2
src_val			RN 3
dst_val			RN 4
src_addr		RN 5
dst_addr		RN 6
				PUSH {R0-R12, LR}
				MRS	R1, psp	
				LDR R0, [R1, #24]	;0x000000DC
				LDR R0, [R0,#-4]	;0x000000D8
				BIC R0, #0xFF000000
				LSR R0, #16;SVC Number(bit[16..=23] of SVC instruction
				
RESET_FX		CMP R0,#7; RESET(n)
				BGT NOP_FX
				MOV R7,#0
				STR R7,[SP,R0,LSL #2];[sp+rn*4]=0
				
				B uscita
				
NOP_FX			CMP R0,#15;NOP
				BLE uscita
				
				CMP R0,#63;unimplemented code(from 16 to 63)
				BLE uscita
				
MEMCPY			CMP R0,#127;MEMCPY(src,dest,n)
				BGT uscita
				AND num,R0,#0x3F;isolate svc_numb[0..=6]
				MOV R7,num
				LDR src_addr,[r1,#32];param#1(r0)
				LDR dst_addr,[r1,#36];param#2(r1)
				
loop			CMP num,#0
				BEQ exit_memcpy
				LDRB src_val,[src_addr],#1;loading byte from source to copy
				STRB src_val,[dst_addr],#1;storing byte to destination
				SUB num,num,#1
				B loop

exit_memcpy		STR r7,[r1,#32];overwriting param#1 with number of bytes transferred		

uscita			POP {R0-R12, LR};restoring default values of registers
				BX LR
				
                ENDP
